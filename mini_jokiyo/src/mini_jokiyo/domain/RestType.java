package mini_jokiyo.domain;

public class RestType {
	private int typeId;
	private char name;
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	public char getName() {
		return name;
	}
	public void setName(char name) {
		this.name = name;
	}

}
